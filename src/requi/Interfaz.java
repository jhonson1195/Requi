    public boolean ComprobarCedula(){
        if("".equals(jTextField1.getText())){
            JOptionPane.showMessageDialog(null,"Por favor ingreso un numero de cedula");
            return false;
        }
        
        try {
              
        ResultSet rs = null;
        Statement cmd = null;
        cmd = con.createStatement();
        rs = cmd.executeQuery("select Nombre from `Clinica`.`Paciente` where `Cedula` =" + jTextField1.getText());
       
        
        if(rs.next()){
            while (rs.next()) {
            Nombre=rs.getString("Nombre");
            }
            
        }else{
            JOptionPane.showMessageDialog(null,"Paciente no registrado");
            return false;
        }
        
        }catch (SQLException ex){
            System.out.println("SQLException: " + ex.getMessage());
        }
        
     return true;
    }
    
