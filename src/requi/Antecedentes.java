    public void CargarDatos(Connection con, String Cedula, String Nombre){
        try {
              
        ResultSet rs = null;
        Statement cmd = null;
        cmd = con.createStatement();
        rs = cmd.executeQuery("select * from `Clinica`.`Antecedentes` where Paciente_Cedula =" + Cedula);
        
        while (rs.next()) {
            jLabel28.setText(Nombre);
            jLabel19.setText("<html><p>"+rs.getString("Patalogicos")+"</p></html>");
            jLabel20.setText("<html><p>"+rs.getString("Alergias")+"</p></html>");
            jLabel21.setText("<html><p>"+rs.getString("Operaciones")+"</p></html>");
            jLabel22.setText("<html><p>"+rs.getString("Transfuciones")+"</p></html>");
            jLabel23.setText("<html><p>"+rs.getString("Traumaticos")+"</p></html>");
            jLabel24.setText("<html><p>"+rs.getString("Toxicos")+"</p></html>");
            jLabel25.setText("<html><p>"+rs.getString("Familiares")+"</p></html>");
            jLabel18.setText("<html><p>"+rs.getString("Enfermedad")+"</p></html>");
            
        }

        rs.close();
        }catch (SQLException ex){
            System.out.println("SQLException: " + ex.getMessage());
        }
        
    }

